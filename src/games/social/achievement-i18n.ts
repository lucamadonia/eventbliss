export function getAchievementText(
  id: string,
  lang?: string,
): AchievementTranslation {
  const l = lang || i18n.language?.split('-')[0] || 'de';
  return (
    ACHIEVEMENT_TRANSLATIONS[id]?.[l] ||
    ACHIEVEMENT_TRANSLATIONS[id]?.de ||
    { name: id, description: '' }
  );
}
